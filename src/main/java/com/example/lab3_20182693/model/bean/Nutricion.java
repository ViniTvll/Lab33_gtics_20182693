package com.example.lab3_20182693.model.bean;

public class Nutricion {

    private int nutricioId;
    private int calorias;
    private int grasas;
    private int proteinas;
    private int carbohidratos;
    private int fibra;


    public int getNutricioId() {
        return nutricioId;
    }

    public void setNutricioId(int nutricioId) {
        this.nutricioId = nutricioId;
    }

    public int getCalorias() {
        return calorias;
    }

    public void setCalorias(int calorias) {
        this.calorias = calorias;
    }

    public int getGrasas() {
        return grasas;
    }

    public void setGrasas(int grasas) {
        this.grasas = grasas;
    }

    public int getProteinas() {
        return proteinas;
    }

    public void setProteinas(int proteinas) {
        this.proteinas = proteinas;
    }

    public int getCarbohidratos() {
        return carbohidratos;
    }

    public void setCarbohidratos(int carbohidratos) {
        this.carbohidratos = carbohidratos;
    }

    public int getFibra() {
        return fibra;
    }

    public void setFibra(int fibra) {
        this.fibra = fibra;
    }
}
